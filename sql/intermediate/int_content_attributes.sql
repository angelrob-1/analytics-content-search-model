SELECT
    POST_ID,
    MD5(COALESCE(CANONICAL_URL, POST_URL)) AS CONTENT_KEY,
    CANONICAL_URL,
    POST_URL,
    TITLE,
    CATEGORY,
    AUTHOR,
    TEAM,
    TAGS,
    PUBLISH_DATE,
    UPDATE_DATE,
    PRODUCT_CATALOG_COUNT,
    PRODUCT_EMBED_COUNT,

    CASE
        WHEN UPDATE_DATE IS NOT NULL THEN 'Updated'
        ELSE 'Not Updated'
    END AS REFRESH_STATUS,

    DATEDIFF('day', PUBLISH_DATE, CURRENT_DATE()) AS CONTENT_AGE_DAYS,

    CASE
        WHEN DATEDIFF('day', PUBLISH_DATE, CURRENT_DATE()) < 365 THEN '0-1 Year'
        WHEN DATEDIFF('day', PUBLISH_DATE, CURRENT_DATE()) < 1095 THEN '1-3 Years'
        WHEN DATEDIFF('day', PUBLISH_DATE, CURRENT_DATE()) < 1825 THEN '3-5 Years'
        ELSE '5+ Years'
    END AS CONTENT_AGE_BUCKET,

    CASE
        WHEN COALESCE(PRODUCT_CATALOG_COUNT, 0) > 0
          OR COALESCE(PRODUCT_EMBED_COUNT, 0) > 0
        THEN TRUE
        ELSE FALSE
    END AS MONETIZED_FLAG

FROM STAGING.STG_CONTENT_METADATA;
