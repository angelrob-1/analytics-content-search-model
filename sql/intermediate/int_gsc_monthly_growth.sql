SELECT
    URL,
    MD5(URL) AS CONTENT_KEY,
    MONTH_DATE,
    CLICKS,

    LAG(CLICKS) OVER (
        PARTITION BY URL
        ORDER BY MONTH_DATE
    ) AS PREVIOUS_MONTH_CLICKS,

    ROUND(
        (CLICKS - LAG(CLICKS) OVER (
            PARTITION BY URL
            ORDER BY MONTH_DATE
        ))
        / NULLIF(LAG(CLICKS) OVER (
            PARTITION BY URL
            ORDER BY MONTH_DATE
        ), 0),
        4
    ) AS MOM_CLICK_GROWTH_PCT

FROM STAGING.STG_GSC_MONTHLY_CLICKS;
